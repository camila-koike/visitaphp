            <h2>Itaipu</h2>
           <p>Itaipu, que em Tupi Guarani significa “a pedra que canta”, é a usina hidrelétrica que mais gera energia em todo o mundo. A usina possui 20 unidades geradoras, o que significa que, em condições de clima favoráveis (chuvas em níveis normais), a produção pode chegar a 100 bilhões de quilowatts-hora. Utilizando o potencial do rio Paraná no trecho em que o rio passa pelo estado do Paraná, a Usina de Itaipu é uma empresa internacional, e não estatal como pode parecer. Segundo informações da própria empresa, 19,3% da energia consumida no Brasil e 87,3% da energia consumida pelo Paraguai são fornecidas pela Itaipu.

